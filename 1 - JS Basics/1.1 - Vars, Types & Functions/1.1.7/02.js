// Crea una función que reemplace todas las 'a' de un texto por 'i'

// 👇🏻 Define aquí tu función
const replaceA = (text) => {
  text.replaceAll('a', 'i');
};
// 👆🏻

replaceA('hola que tal'); // holi que til
replaceA('me llamo Carlos'); // me llimo Cirlos

